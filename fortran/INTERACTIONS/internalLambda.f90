        real function internalLambda (tau)
        implicit none
! Argument Declaration and Description
! ===========================================================================
! Input
        real, intent(in) :: tau
! Procedure
! ===========================================================================
        internalLambda = 1-tau**5*(70*tau**4-315*tau**3+540*tau**2-420*tau+126)
! Deallocate Arrays
! ===========================================================================
! Format Statements
! ===========================================================================
        return
        end
